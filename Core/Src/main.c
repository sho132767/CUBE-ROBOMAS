void CAN_Send_Current(int16_t current)
{
    CAN_TxHeaderTypeDef TxHeader;
    uint8_t TxData[8] = {0};
    uint32_t TxMailbox;

    if (current > 10000) current = 10000;     
    if (current < -10000) current = -10000;

    TxHeader.StdId = CAN_TX_ID;               // 0x200
    TxHeader.ExtId = 0;
    TxHeader.IDE   = CAN_ID_STD;
    TxHeader.RTR   = CAN_RTR_DATA;
    TxHeader.DLC   = 8;
    TxHeader.TransmitGlobalTime = DISABLE;

    uint8_t offset = (MOTOR_ID - 1) * 2;      // MOTOR_ID=1 → Byte0/1
    TxData[offset]     = (uint8_t)((current >> 8) & 0xFF);
    TxData[offset + 1] = (uint8_t)( current       & 0xFF);

    if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox) != HAL_OK)
    {
        Error_Handler();
    }
}



/**
  * @brief
  * @note   M2006:
  *         ID: 0x201-0x208
  *         Byte0-1: (0-8191)
  *         Byte2-3:  (RPM)
  *         Byte4-5:()
  *         Byte6:    (°C)
  */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    CAN_RxHeaderTypeDef RxHeader;
    uint8_t RxData[8];

    if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData) == HAL_OK)
    {

        uint32_t expected_id = 0x200 + MOTOR_ID;

        if (RxHeader.StdId == expected_id && RxHeader.IDE == CAN_ID_STD)
        {

            motor_angle = (RxData[0] << 8) | RxData[1];      
            motor_rpm = (RxData[2] << 8) | RxData[3];        
            motor_current = (RxData[4] << 8) | RxData[5];    
            motor_temp = RxData[6];                           
        }
    }
}

